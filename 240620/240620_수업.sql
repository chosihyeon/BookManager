SELECT BRAND_CD
,COUNT(INVOICE_NO)
,COUNT(MAX(OUTBOUND_DATE)-MIN(OUTBOUND_DATE))AS DAY_CNT
,MIN(OUTBOUND_DATE)AS DATE_MIN
,MAX(OUTBOUND_DATE)AS DATE_MAX
FROM A_OUT_M
GROUP BY BRAND_CD,
ORDER BY BRAND_CD;

