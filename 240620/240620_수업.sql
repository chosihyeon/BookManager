SELECT BRAND_CD
,ITEM_CD
,SUM(ORDER_QTY)AS ORDER_QTY
,CASE WHEN (ORDER_QTY/2)=0 THEN '홀' ELSE '짝' 
END AS EVENODD
FROM A_OUT_D 
GROUP by BRAND_CD, ITEM_CD, CASE WHEN (ORDER_QTY/2)=0 THEN '홀' ELSE '짝' END
ORDER BY BRAND_CD, ITEM_CD, CASE WHEN (ORDER_QTY/2)=0 THEN '홀' ELSE '짝' END ,ORDER_QTY DESC;
;

