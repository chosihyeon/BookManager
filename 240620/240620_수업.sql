SELECT BRAND_CD,ITEM_CD,SUM(ORDER_QTY)AS SUM_QTY
,CASE WHEN SUM(ORDER_QTY)>=5 THEN '상'
,CASE WHEN SUM(ORDER_QTY)=3 OR SUM(ORDER_QTY)=4 THEN '중'
,CASE WHEN SUM(ORDER_QTY)=1 OR SUM(ORDER_QTY)=2 THEN '하'
END AS GRADE 
FROM A_OUT_D
GROUP BY BRAND_CD
ORDER BY BRAND_CD
,CASE SUM(ORDER_QTY) >=5 THEN '상'
,CASE  SUM(ORDER_QTY)=3 OR SUM(ORDER_QTY)=4 THEN '중'
,CASE SUM(ORDER_QTY)=1 OR SUM(ORDER_QTY)=2 THEN '하'
END;

